CREATE TABLE KLIENT(
	ID_KLIENT DECIMAL(4)NOT NULL,
	IMIE VARCHAR(50) NOT NULL,
	NAZWISKO VARCHAR(50) NOT NULL,
	ADRES VARCHAR(50),
	DATA_ZAISTNIENIA DATE
	CONSTRAINT [PK_KLIENT] PRIMARY KEY CLUSTERED([ID_KLIENT] ASC)
)

CREATE TABLE LOKAL(
	ID_LOKALU DECIMAL(4) NOT NULL,
	NAZWA VARCHAR(50) NOT NULL,
	ADRES VARCHAR(50) NOT NULL
	CONSTRAINT [PK_LOKAL] PRIMARY KEY CLUSTERED([ID_LOKALU] ASC)
)

CREATE TABLE RODZAJ_USLUGI(
	ID_RODZAJU DECIMAL(4) NOT NULL,
	NAZWA VARCHAR(50) NOT NULL,
	CENA NUMERIC(10,2) NOT NULL,
	CZAS_TRWANIA DECIMAL(10) NOT NULL
	CONSTRAINT [PK_RODZAJ_USLUGI] PRIMARY KEY CLUSTERED(ID_RODZAJU ASC)
)
CREATE TABLE PRACOWNIK(
	ID_PRACOWNIKA DECIMAL(4) NOT NULL,
	IMIE VARCHAR(50) NOT NULL,
	NAZWISKO VARCHAR(50) NOT NULL,
	DATA_ZATRUDNIENIA DATE,
	DATA_ZWOLNIENIA DATE,
	PENSJA NUMERIC(10,2) NOT NULL,
	DODATEK_DO_PENSJI NUMERIC(10,2)
	CONSTRAINT [PK_PRACOWNIK] PRIMARY KEY CLUSTERED(ID_PRACOWNIKA ASC)
)
CREATE TABLE USLUGA_FRYZJERSKA(

	ID_USLUGI DECIMAL(4) NOT NULL,
	DATA_I_CZAS_REZERWACJI DATETIME,
	CENA_USLUGI NUMERIC(10,2) NOT NULL,
	DATA_CZAS_WYKONANIA DATETIME,

	CONSTRAINT [PK_USLUGA_FRYZJERSKA] PRIMARY KEY CLUSTERED(ID_USLUGI ASC),

	ID_KLIENT DECIMAL(4) NOT NULL,
	ID_LOKALU DECIMAL(4) NOT NULL,
	ID_RODZAJU DECIMAL(4) NOT NULL,
	ID_PRACOWNIKA DECIMAL(4) NOT NULL

	
	CONSTRAINT [FK_KLIENT] FOREIGN KEY(ID_KLIENT) REFERENCES KLIENT(ID_KLIENT),
	CONSTRAINT [FK_LOKAL] FOREIGN KEY(ID_LOKALU) REFERENCES LOKAL(ID_LOKALU),
	CONSTRAINT [FK_RODZAJ_USLUGI] FOREIGN KEY(ID_RODZAJU) REFERENCES RODZAJ_USLUGI(ID_RODZAJU),
	CONSTRAINT [FK_PRACOWNIK] FOREIGN KEY(ID_PRACOWNIKA) REFERENCES PRACOWNIK(ID_PRACOWNIKA)
)
